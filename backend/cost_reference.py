"""Справочник себестоимости напитков «Дарвина» — рецептурный источник правды.

Перенесено из «Расчет фуд-коста напитков_ кофе и чай.xlsx»
(лист «Расчет фуд-коста напитков кофе»); сводка — в `себестоимость.md`.

Закрывает пробел №1 / шаг №2 из README: без справочника рецептов нельзя было
посчитать маржу по товарам, а себестоимость была «спрятана» в статье «Прочее».

Себестоимость позиции = сумма затрат на компоненты порции. Значения по
компонентам взяты из Excel 1:1 (Excel сам перемножал расход × закупочную цену),
поэтому их сумма точно сходится с контрольным итогом по позиции (CONTROL).

Деньги — только Decimal, как и во всём проекте.
"""
from __future__ import annotations

from decimal import Decimal as D
from typing import Dict, List

# Закупочные цены ингредиентов — для справки и аудита (per-portion суммы ниже
# даны напрямую из Excel, а не пересчитываются отсюда, чтобы сойтись с контролем).
INPUT_PRICES = {
    "кофе_зерно, ₽/кг": D("2200"),
    "молоко, ₽/л": D("94"),
    "сахар, ₽/кг": D("60"),
    "корица, ₽/8г": D("40"),
}

# Базовые кофейные позиции (все, кроме РАФ) по объёму стакана, мл.
# Значение каждого компонента — стоимость на ОДНУ порцию, ₽.
BASE_DRINKS: Dict[int, Dict[str, D]] = {
    250: {
        "кофе": D("38"), "молоко": D("16.92"), "сахар": D("0.60"),
        "корица": D("0.80"), "стакан": D("2.62"), "крышка": D("1.80"),
        "монжет": D("1.75"), "подстаканник": D("2.83"), "палочка": D("0.50"),
        "трубочка": D("0.50"), "стики": D("1.50"),
    },
    350: {
        "кофе": D("38"), "молоко": D("25.38"), "сахар": D("0.60"),
        "корица": D("0.80"), "стакан": D("3.64"), "крышка": D("1.95"),
        "монжет": D("1.75"), "подстаканник": D("2.83"), "палочка": D("0.50"),
        "трубочка": D("0.50"), "стики": D("1.50"),
    },
    450: {
        "кофе": D("38"), "молоко": D("26.32"), "сахар": D("0.60"),
        "корица": D("0.80"), "стакан": D("4.49"), "крышка": D("1.95"),
        "монжет": D("1.75"), "подстаканник": D("2.83"), "палочка": D("0.50"),
        "трубочка": D("0.50"), "стики": D("1.50"),
    },
}

# Допы — прибавляются к себестоимости базовой позиции, ₽ за порцию.
ADDONS: Dict[str, D] = {
    "какао": D("11.90"),          # 10 г порошка
    "ванильный сахар": D("3.84"),  # 10 г порошка
    "матча": D("10.75"),          # 3 г порошка
    "порошки": D("10.515"),       # 15 г порошка
    "сироп": D("6.00"),           # 20 г сиропа
}

# Допы, где в Excel есть закупочная цена, но себестоимость порции не заполнена —
# их нельзя использовать в расчёте, пока не внесён расход на порцию.
ADDONS_NO_COST: List[str] = ["сливки", "арахисовая паста"]

# Контрольные итоги себестоимости позиции из Excel, ₽ — сумма компонентов обязана
# совпасть с этими значениями (аналог EXCEL_ANNUAL в darwin_data).
CONTROL: Dict[int, D] = {
    250: D("67.82"),
    350: D("77.45"),
    450: D("79.24"),
}

ZERO = D("0")


def base_cost(volume_ml: int) -> D:
    """Себестоимость базовой кофейной позиции данного объёма, ₽."""
    if volume_ml not in BASE_DRINKS:
        raise KeyError(f"Нет рецепта для объёма {volume_ml} мл: {sorted(BASE_DRINKS)}")
    return sum(BASE_DRINKS[volume_ml].values(), ZERO)


def drink_cost(volume_ml: int, *addons: str) -> D:
    """Себестоимость напитка: базовая позиция + перечисленные допы, ₽."""
    cost = base_cost(volume_ml)
    for a in addons:
        if a in ADDONS_NO_COST:
            raise ValueError(
                f"Доп «{a}»: себестоимость порции не заполнена в Excel — "
                "нельзя включать в расчёт."
            )
        if a not in ADDONS:
            raise KeyError(f"Неизвестный доп «{a}»: {sorted(ADDONS)}")
        cost += ADDONS[a]
    return cost


def verify_against_excel() -> Dict[int, bool]:
    """Сверяет сумму компонентов с контрольным итогом Excel по каждой позиции."""
    return {v: base_cost(v) == CONTROL[v] for v in BASE_DRINKS}


# --- Аудит заявленных закупочных цен -------------------------------------
# Расход сырья на порцию × цена из INPUT_PRICES должен давать ту же сумму, что
# стоит в расчёте владельца. Где не сходится — повод уточнить у владельца.
# (молоко берём для позиции 250 мл; кофе/сахар/корица одинаковы для всех объёмов.)
RAW_AUDIT: Dict[str, tuple] = {
    # компонент: (расход, ед., ₽/ед., документ. ₽/порцию, примечание)
    "молоко": (D("180"), "мл", D("0.094"), D("16.92"), ""),
    "сахар":  (D("10"),  "г",  D("0.06"),  D("0.60"),  ""),
    "кофе":   (D("17"),  "г",  D("2.2"),   D("38.00"), "владелец округляет 37.40 → 38.00 ₽"),
    "корица": (D("0.5"), "г",  D("5.0"),   D("0.80"),
               "цена 40 ₽/8 г (=5 ₽/г) даёт 2.50 ₽/порцию — расхождение, уточнить"),
}


def audit_input_prices() -> List[dict]:
    """Сверяет заявленные закупочные цены с per-portion суммами расчёта."""
    rows: List[dict] = []
    for name, (dose, unit, price, documented, note) in RAW_AUDIT.items():
        expected = (dose * price).quantize(D("0.01"))
        rows.append(
            {
                "name": name,
                "dose": f"{dose} {unit}",
                "expected": expected,
                "documented": documented,
                "delta": (documented - expected).quantize(D("0.01")),
                "ok": documented == expected,
                "note": note,
            }
        )
    return rows
